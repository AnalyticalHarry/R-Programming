  # integers
  num_integer_1 <- 5L
  num_integer_2 <- 3L
  
  # addition
  add_integers <- num_integer_1 + num_integer_2
  cat("Addition of integers:", add_integers)
  
  # subtraction
  subtract_integers <- num_integer_1 - num_integer_2
  cat("Subtraction of integers:", subtract_integers)
  
  # multiplication
  multiply_integers <- num_integer_1 * num_integer_2
  cat("Multiplication of integers:", multiply_integers)
  
  # division
  divide_integers <- num_integer_1 / num_integer_2
  cat("Division of integers:", divide_integers)
  
  # modulus (remainder of division)
  modulus_integers <- num_integer_1 %% num_integer_2
  cat("Modulus (remainder of division) of integers:", modulus_integers)
  
